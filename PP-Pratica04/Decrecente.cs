using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

/*
 * Elabore um programa em C# que exiba todos os números entre 1 e 20, de forma
 * decrescente.
*/

namespace PP_Pratica04
{
    class Decrecente
    {
        public void Decrescente()
        {
            for (int i = 20; i >=0; i--)
            {
                Console.WriteLine(i);
            }
        }
    }
}
